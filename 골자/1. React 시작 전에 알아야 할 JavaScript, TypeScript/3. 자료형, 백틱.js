// 이 다음에 ppt 로 가서 백틱 설명

// 실습4. 백틱(``). 키보드 1 옆에 있는 이상하게 생긴 따옴표.
// 주로 문자열 안에서 데이터를 쓰려고 사용
// 백틱을 사용하지 않을 경우, + 를 사용해 여러개 문자열을 이어붙여야
// + 는 문자열에선 덧셈이 아니라 이어붙인다는뜻
const myName = '조교행님';
const age = 28;
console.log(myName + '은 ' + age + '살이다');

// 백틱을 사용하면, 한번에 적을 수 있음
// 백틱 안에서 변수를 표현할 땐 이와 같이, ${변수이름}을 사용
const myName2 = '조교행님';
const age2 = 28;
console.log(`${myName2}은 ${age2}살이다`);

// 위 콘솔로그의 변형. 줄바꿈 적용됨
// 백틱을 사용하면 백틱 안에서 엔터를 쳤을 때 줄바꿈이 그대로 적용되므로, 
// console.log()를 몇 줄씩이나 쓸 필요가 없다.
// 엔터나 탭이 그대로 적용된 것을 볼 수 있음
console.log(`
	${myName2}은
	${age2}살이다
`);

