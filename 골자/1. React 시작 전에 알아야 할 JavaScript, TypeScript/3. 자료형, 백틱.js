// 실습1. 0과 '0'
// 우리에겐 똑같은 0인데 컴퓨터는 다르게 인식
const a = 0, b = '0';
console.log(typeof a, typeof b);
// 둘을 비교해서 같은지 알아보자. === 는 다음 장에서 배울 비교연산자라고 함
// 결과가 맞으면 true, 틀리면 false
console.log(0 === '0');

// 실습2. 스트링, (문자열)은 항상 따옴표 쳐줘야.
